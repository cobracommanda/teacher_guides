# Function to collect and sort paragraphs by Generic_sidebar_* styles
def collect_generic_sidebar_paragraphs(data):
    paragraphs = []
    for style, items in data["paragraph"].items():
        if style.startswith("Generic_sidebar_"):
            for item in items:
                paragraphs.append({
                    "style": style,
                    "storyIndex": item["storyIndex"],
                    "paragraphIndex": item["paragraphIndex"],
                    "pageNumber": item["pageNumber"],
                    "contents": item["contents"]
                })
    return sorted(paragraphs, key=lambda x: (x["storyIndex"], x["paragraphIndex"]))

# Function to format the content into an HTML table with the specified format
